# Input the string “Python”, delete 2 characters and reverse

print("The string is :")
print("Python")
A = 'Python'
B = A[2:6]          # Deleting the first two letters of Python
print("The string after deleting the first two letters: ")
print(B)            # Printing the output
C = B[::-1]         # Reversing the output obtained
print("The reversed string is: ")
print(C)            # Printing the output